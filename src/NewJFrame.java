    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
           
            
        int i=jTable1.getSelectedRow();
        if(i>=0)
        {
            Double price = Double.parseDouble(Price.getText());
            Integer count = Integer.parseInt(Amount.getText());
            Double total = price*count;
            model.setValueAt(ID.getText(), i, 0);
            model.setValueAt(Name.getText(), i, 1);
            model.setValueAt(Price.getText(), i, 2);
            model.setValueAt(Amount.getText(), i, 3);
            model.setValueAt(total, i, 4);

            JOptionPane.showMessageDialog(null, "Invoice Updated Successfully");
            updateItemsTotalLabel();
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Please select an Invoice to be updated");
            
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int i = jTable1.getSelectedRow();
        if( i>=0)
        {
            model.removeRow(i);
            JOptionPane.showMessageDialog(null, "Invoice Deleted Successfully");
            updateItemsTotalLabel();

        }
        else
        {
            JOptionPane.showMessageDialog(null, "Please select an invoice to delete");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

