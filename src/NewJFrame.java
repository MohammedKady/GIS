    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        if(ID.getText().equals("") ||Name.getText().equals("") ||Amount.getText().equals("") ||Price.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null, "Please fill all the invoice componenets.");
        }

        else

        {
            Double price = Double.parseDouble(Price.getText());
            Integer count = Integer.parseInt(Amount.getText());
            Double total = price*count;
            model.addRow(new Object[] {ID.getText(),Name.getText(),Price.getText(), Amount.getText(),total});
            ID.setText("");
            Name.setText("");
            Amount.setText("");
            Price.setText("");
            JOptionPane.showMessageDialog(null, "Invoice Immported Successfully");
            updateItemsTotalLabel();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int select = jTable1.getSelectedRow();
        ID.setText(model.getValueAt(select, 0).toString());
        Name.setText(model.getValueAt(select, 1).toString());
        Price.setText(model.getValueAt(select, 2).toString());
        Amount.setText(model.getValueAt(select, 3).toString());
    }//GEN-LAST:event_jTable1MouseClicked

   private void updateItemsTotalLabel() 
   {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    int r = model.getRowCount();
    int c = model.getColumnCount();
    Double sum = new Double(0);
    for (int i=0; i < r; i++) { 
        sum += Double.parseDouble( model.getValueAt(i, c-1).toString());
    }
itemsTotalLabel.setText(Double.toString(sum));  

   }
           
